int		gimme_value(char **tab, char c)
{
	int num;
	int i;
	int j;

	i = 0;
	num = 0;
	while (tab[i])
	{
		j = 0;
		while (tab[i][j])
		{
			if (c == tab[i][j])
				num++;
			j++;
		}
		i++;
	}
	return (num);
}
